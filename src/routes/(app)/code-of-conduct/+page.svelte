<h1>
	Group Code of Conduct
</h1>

<p>
All members of the research group, along with visitors, are required to agree with the 
following code of conduct. Additionally, we collectively agree to enforce this code. 
We expect cooperation from all members to help ensuring a safe environment for everybody.
</p>

<Linkable id="short">The Quick Version</Linkable>

<p>
This research group is dedicated to providing a harassment-free experience for everyone, 
regardless of gender, gender identity and expression, age, sexual orientation, disability, 
physical appearance, body size, race, ethnicity, religion (or lack thereof), or technology 
choices. We do not tolerate harassment of group members or other colleagues in any form. 
Sexual language and imagery is not appropriate for any professional setting, including 
talks, workshops, parties, Twitter and other online media. Research group members 
violating these rules may be sanctioned or expelled from the group at the discretion 
of Kyle Niemeyer.
</p>

<Linkable id="short">The Less Quick Version</Linkable>

<p>
Harassment includes offensive verbal comments related to gender, gender identity and 
expression, age, sexual orientation, disability, physical appearance, body size, race, 
ethnicity, religion, technology choices, sexual images in public spaces, deliberate 
intimidation, stalking, following, harassing photography or recording, sustained 
disruption of the research environment, talks, or other events, inappropriate physical 
contact, unwelcome sexual attention, or creation of an otherwise sexualized environment.
</p>

<p>
Group members asked to stop any harassing behavior are expected to comply immediately.
</p>

<p>
If a member engages in harassing behavior, other group members and Kyle Niemeyer may 
take any action they deem appropriate to stop this behavior, including warning the 
offender or expulsion from the group and sanctioning through official Oregon State 
University channels.
</p>

<p>
If you are being harassed, notice that someone else is being harassed, or have any other 
concerns, please contact Kyle immediately via email, Slack direct message, phone 
call/text message, or in person. Kyle is available to help group members contact security 
or local law enforcement, navigate official University reporting channels, or otherwise 
assist those experiencing harassment to feel safe in the research environment.
</p>

<p>
If Kyle is the cause of the concern or you would prefer he not be involved, official 
concerns can be addressed by contacting 
<External to="mailto:andy.dong@oregonstate.edu">Andy Dong</External> in the School of MIME, 
or the <External to="http://eoa.oregonstate.edu">Oregon State University Equal Opportunity and Access office</External>.
The <External to="https://ombuds.oregonstate.edu">University Ombuds Office</External> is 
also available if you feel you are being subjected to disrespectful or inappropriate 
conduct, or conduct that might constitute bullying.
</p>

<p>
We expect research group members to follow these rules in all professional settings 
including the office, conferences, workshop venues, and group-related social events. 
We additionally expect members to treat each other professionally with respect to 
their work and workspaces, with constructive criticism, without dismissive behavior, 
and without disruption of shared working spaces.
</p>

